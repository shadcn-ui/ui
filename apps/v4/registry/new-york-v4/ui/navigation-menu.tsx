const navigationMenuTriggerStyle = cva(
  "group inline-flex h-9 w-max items-center justify-center rounded-[var(--radius-md)] bg-transparent px-4 py-2 text-sm font-medium transition-colors duration-[100ms] outline-none hover:bg-[rgb(var(--foreground)/0.06)] hover:text-[rgb(var(--foreground))] focus:bg-[rgb(var(--foreground)/0.06)] focus:text-[rgb(var(--foreground))] focus-visible:ring-2 focus-visible:ring-[rgb(var(--ring)/0.4)] disabled:pointer-events-none disabled:opacity-40 data-[state=open]:bg-[rgb(var(--foreground)/0.06)] data-[state=open]:text-[rgb(var(--foreground))]"
)

function NavigationMenuTrigger({
  className,
  children,
  ...props
}: React.ComponentProps<typeof NavigationMenuPrimitive.Trigger>) {
  return (
    <NavigationMenuPrimitive.Trigger
      data-slot="navigation-menu-trigger"
      className={cn(navigationMenuTriggerStyle(), "group", className)}
      {...props}
    >
      {children}{" "}
      <ChevronDownIcon
        className="relative top-[1px] ml-1 size-3 transition duration-300 group-data-[state=open]:rotate-180"
        aria-hidden="true"
      />
    </NavigationMenuPrimitive.Trigger>
  )
}

