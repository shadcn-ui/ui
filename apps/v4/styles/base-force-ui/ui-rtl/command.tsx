"use client"

import * as React from "react"
import { Command as CommandPrimitive, useCommandState } from "cmdk"
import { CheckIcon, SearchIcon } from "lucide-react"

import { cn } from "@/lib/utils"
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogHeader,
  DialogTitle,
} from "@/styles/base-force-ui/ui-rtl/dialog"
import {
  InputGroup,
  InputGroupAddon,
} from "@/styles/base-force-ui/ui-rtl/input-group"

function CommandResultsAnnouncer() {
  const count = useCommandState((state) => state.filtered.count)
  // stays silent at zero — CommandEmpty's own role="status" announces the
  // no-results copy instead, avoiding two announcements for one state
  return (
    <span role="status" aria-live="polite" className="sr-only">
      {count > 0 ? `${count} ${count === 1 ? "result" : "results"}` : ""}
    </span>
  )
}

function Command({
  className,
  children,
  ...props
}: React.ComponentProps<typeof CommandPrimitive>) {
  return (
    <CommandPrimitive
      data-slot="command"
      className={cn(
        "flex size-full flex-col overflow-hidden rounded-xl! bg-popover p-1 text-popover-foreground",
        className
      )}
      {...props}
    >
      {children}
      {/* [FORCE-UI] WCAG 4.1.3 — announces the filtered result count as the user types */}
      <CommandResultsAnnouncer />
    </CommandPrimitive>
  )
}

function CommandDialog({
  title = "Command Palette",
  description = "Search for a command to run...",
  children,
  className,
  showCloseButton = false,
  ...props
}: Omit<React.ComponentProps<typeof Dialog>, "children"> & {
  title?: string
  description?: string
  className?: string
  showCloseButton?: boolean
  children: React.ReactNode
}) {
  return (
    <Dialog {...props}>
      <DialogHeader className="sr-only">
        <DialogTitle>{title}</DialogTitle>
        <DialogDescription>{description}</DialogDescription>
      </DialogHeader>
      <DialogContent
        className={cn(
          "top-1/3 translate-y-0 overflow-hidden rounded-xl! p-0",
          className
        )}
        showCloseButton={showCloseButton}
      >
        {children}
      </DialogContent>
    </Dialog>
  )
}

